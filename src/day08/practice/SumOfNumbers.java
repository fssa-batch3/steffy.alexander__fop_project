package day08.practice;

public class SumOfNumbers {
	public static void main(String[] args) {

		int num = 10;

		int addnum = 0;
		for (int i = 0; i <= num; i++) {
			// adding the number using for loop
			addnum += i;
			System.out.println(addnum);
			// printing the added number
		}
	}
}
